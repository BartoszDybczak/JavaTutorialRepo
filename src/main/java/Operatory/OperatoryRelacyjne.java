package Operatory;

public class OperatoryRelacyjne {

    public static void main(String[] args) {

        boolean isTrue = 4 == 4;

        System.out.println(isTrue);
    }

    /*
               Operatory relacyjne (porównania)

               == - równa się
               != nie równa sie/różnica
               > - większe od
               < - mniejsze od
               >= - większe bądź równe
               <= - mniejsze bądź równe
     */
}